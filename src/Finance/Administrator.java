package lesson5;

public class Administrator extends DeptEmployee {
 
	 int overtimeHours;
	Administrator(String name, double salary, int yearOfHire, int monthOfHire, int dayOfHire,int overtimeHours) {
		super(name, salary, yearOfHire, monthOfHire, dayOfHire);
		 this.overtimeHours=overtimeHours;
	
	}
	
	public double computeSalary(){
		return salary=salary +(20*overtimeHours);
	}
}

