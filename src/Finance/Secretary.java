package lesson5;

public class Secretary extends DeptEmployee {
	int overtimeHours;
	
	Secretary(String name, double salary, int yearOfHire, int monthOfHire, int dayOfHire,int overtimeHours) {
		super(name, salary, yearOfHire, monthOfHire, dayOfHire);
		 this.overtimeHours=overtimeHours;
	}



	public double computeSalary(){
		return salary=salary +(12*overtimeHours);
	}

}
